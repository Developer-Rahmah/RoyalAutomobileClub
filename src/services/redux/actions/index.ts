export const setShowGifImageAction = (showGifImage: string) => ({
  type: 'SET_SHOW_GIF_ICON',
  payload: showGifImage,
});
export const setLanguageAction = (langCode: string) => ({
  type: 'SET_LANGUAGE',
  payload: langCode,
});
export const setPasswordAction = (password: string) => ({
  type: 'SET_PASSWORD',
  payload: password,
});
